package flower.fruit.shop.domain;

public class UserAddress {

	private String ua_id;
	private String userId;
	private String ua_name;
	private String ua_mobile;
	private String ua_province;
	private String ua_detal;
	private String ua_price;
	public String getUa_id() {
		return ua_id;
	}
	public void setUa_id(String ua_id) {
		this.ua_id = ua_id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUa_name() {
		return ua_name;
	}
	public void setUa_name(String ua_name) {
		this.ua_name = ua_name;
	}
	public String getUa_mobile() {
		return ua_mobile;
	}
	public void setUa_mobile(String ua_mobile) {
		this.ua_mobile = ua_mobile;
	}
	public String getUa_province() {
		return ua_province;
	}
	public void setUa_province(String ua_province) {
		this.ua_province = ua_province;
	}
	public String getUa_detal() {
		return ua_detal;
	}
	public void setUa_detal(String ua_detal) {
		this.ua_detal = ua_detal;
	}
	public String getUa_price() {
		return ua_price;
	}
	public void setUa_price(String ua_price) {
		this.ua_price = ua_price;
	}
	
	
}
